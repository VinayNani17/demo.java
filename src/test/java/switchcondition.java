public class switchcondition {
    public static void main(String[] args) {
        int a=1,b=4;
        switch(a+b){
            case 1:
                System.out.println("monday");
                break;
            case 2:
                System.out.println("tuesday");
                break;
            case 3:
                System.out.println("wednesday");
                break;
            default:
                System.out.println("default");
            }
        System.out.println("week");
        }
    }

