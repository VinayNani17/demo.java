
public class forloop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("nani");
        }
        for (int j = 5; j >= 0; j--) {
            System.out.println(j);
       System.out.println("default");
        }
        for (int k = 0; k < 10; k = k + 2) {
            System.out.println(k);
        }
    }
}